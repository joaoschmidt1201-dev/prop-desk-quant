csv_path = os.path.join(out_dir, "fase1_2024.csv")
cols = ["open_date","side","lower","center","upper","wing","entry_debit",
        "S_entry","iv","S_settle","payoff","return","net_pl","r2r","zone","result"]
with open(csv_path, "w", newline="") as f:
    w = csv.writer(f)
    w.writerow(cols)
    for (d,side,lo,ce,up,deb,Se,iv,St,pay,net,z) in ROWS:
        ret = (pay-deb)/deb if deb>0 else 0.0
        r2r = (WING-deb)/deb if deb>0 else 0.0
        w.writerow([d,side,lo,ce,up,WING,deb,Se,iv,St,pay,round(ret,4),net,round(r2r,2),z,
                    "W" if net>0 else "L"])

# ---- distribuicao ----
nets = [r[10] for r in ROWS]
n = len(nets)
wins = [x for x in nets if x>0]
losses = [x for x in nets if x<=0]
total = sum(nets)
gp, gl = sum(wins), -sum(losses)
pf = gp/gl if gl else float('inf')

# multiplo do winner = net / risco (risco = debit*100)
risk = [r[5]*100 for r in ROWS]
win_mult = [r[10]/(r[5]*100) for r in ROWS if r[10]>0]

# equity curve + max drawdown (sobre P/L acumulado)
eq, peak, mdd, run = [], 0.0, 0.0, 0.0
peak_val, peak_idx = -1e9, -1
for i,x in enumerate(nets):
    run += x
    eq.append(run)
    if run > peak_val:
        peak_val, peak_idx = run, i
    peak = max(peak, run)
    mdd = max(mdd, peak-run)

# Sharpe por-trade e anualizado (~72 trades/ano)
rets_pct = [r[10]/(r[5]*100) for r in ROWS]   # retorno por trade em % do risco
mean_r, sd_r = st.mean(rets_pct), st.pstdev(rets_pct)
sharpe_trade = mean_r/sd_r if sd_r else 0.0
sharpe_ann = sharpe_trade * (n**0.5)

# distribuicao por zona
zones = {0:0,1:0,2:0,3:0}
for r in ROWS: zones[r[11]] += 1

