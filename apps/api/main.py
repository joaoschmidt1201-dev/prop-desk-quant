def _fmt_money(value: float) -> str:
    return f"${value:,.0f}"


def _build_scope_metrics(trades: list[dict[str, Any]], individual: dict[str, float]) -> str:
    open_weekday_pnl: dict[str, float] = {}
    close_weekday_pnl: dict[str, float] = {}
    holding_days: list[int] = []

    for t in trades:
        pnl = trade_pnl(t, individual)
        open_day = trade_open_weekday(t)
        open_weekday_pnl[open_day] = open_weekday_pnl.get(open_day, 0.0) + pnl

        close_day = trade_close_weekday(t)
        if close_day != "Unknown":
            close_weekday_pnl[close_day] = close_weekday_pnl.get(close_day, 0.0) + pnl

        days = trade_days_held(t)
        if days is not None:
            holding_days.append(days)

    def fmt_weekday_totals(rows: dict[str, float]) -> str:
        if not rows:
            return "unavailable"
        return ", ".join(
            f"{_weekday_label(day)}={_fmt_money(rows[day])}"
            for day in sorted(rows, key=_weekday_sort_key)
        )

    if holding_days:
        avg_days = sum(holding_days) / len(holding_days)
        holding_desc = f"n={len(holding_days)}, avg={avg_days:.2f} calendar days"
        holding_answer = f"avg={avg_days:.2f} calendar days over n={len(holding_days)} closed trades"
    else:
        holding_desc = "unavailable"
        holding_answer = "unavailable"

    best_open = max(open_weekday_pnl.items(), key=lambda item: item[1]) if open_weekday_pnl else None
    best_close = max(close_weekday_pnl.items(), key=lambda item: item[1]) if close_weekday_pnl else None
    best_open_desc = f"{_weekday_label(best_open[0])}={_fmt_money(best_open[1])}" if best_open else "unavailable"
    best_close_desc = f"{_weekday_label(best_close[0])}={_fmt_money(best_close[1])}" if best_close else "unavailable"

    return (
        "Authoritative computed metrics in scope:\n"
        "- Weekday key: Mon=Monday, Tue=Tuesday, Wed=Wednesday, Thu=Thursday, Fri=Friday.\n"
        f"- Best opening weekday by PnL: {best_open_desc}\n"
        f"- Best inferred close weekday by PnL: {best_close_desc}\n"
        f"- Opening weekday PnL: {fmt_weekday_totals(open_weekday_pnl)}\n"
        f"- Inferred close weekday PnL: {fmt_weekday_totals(close_weekday_pnl)}\n"
        f"- Closed-trade holding period from inferred close dates: {holding_desc}\n"
        f"- If asked average holding time, copy this exact metric: {holding_answer}\n"
    )


