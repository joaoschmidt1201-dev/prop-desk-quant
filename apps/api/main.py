def parse_months(months: str | None) -> list[str]:
    if not months:
        return []
    parsed = [m.strip() for m in months.split(",") if m.strip()]
    return sorted(parsed, key=_month_sort_tuple)


def trade_in_filter(trade: dict[str, Any], months: list[str], env: str | None) -> bool:
    sheet = (trade.get("sheet") or "").strip()
    valid_sheet = bool(sheet and MONTH_SHEET_REGEX.match(sheet))
    if months and (not valid_sheet or sheet not in months):
        return False
    if env and trade.get("environment") != env:
        return False
    return True


def trade_pnl(trade: dict[str, Any], individual: dict[str, float]) -> float:
    """Best-effort PnL extraction across snapshot field name variants."""
    if trade.get("name") in individual:
        return float(individual[trade["name"]])
    for key in ("pnl_current", "open_pnl", "pnl"):
        v = trade.get(key)
        if v is not None:
            return float(v)
    return 0.0


def trade_delta(trade: dict[str, Any]) -> float:
    for key in ("delta_current", "delta"):
        v = trade.get(key)
        if v is not None:
            return float(v)
    return 0.0


def sheet_summary_lookup(snap: dict[str, Any]) -> dict[str, dict[str, Any]]:
    summaries: dict[str, dict[str, Any]] = {}
    for env_name, env_summaries in (snap.get("sheet_summaries") or {}).items():
        for summary in env_summaries or []:
            sheet = summary.get("month")
            if isinstance(sheet, str):
                summaries[sheet] = {**summary, "env": env_name}
    return summaries


def strategy_family(name: str) -> str:
    n = name.upper()
    if "IRON CONDOR" in n or " IC" in n or "IC7" in n or "IC8" in n:
        return "Iron Condor"
    if "RJL" in n:
        return "RJL"
    if "BATMAN" in n or " BAT" in n or "BAT42" in n or "BAT7" in n:
        return "Batman / BWB"
    if "CALL BEAR" in n or "BEAR CALL" in n:
        return "Bear Call"
    if "BULL PUT" in n or "PUT CREDIT" in n or "PCS" in n:
        return "Bull Put"
    if "BULL CALL" in n:
        return "Bull Call"
    if "HALF-CALL" in n or "CALL-HALF" in n:
        return "Half Call"
    if "PUT-HALF" in n or "PUT BROKEN" in n:
        return "Put Broken Wing"
    return "Other"


def parse_iso_date(value: Any) -> datetime | None:
    if not value:
        return None
    try:
        return datetime.fromisoformat(str(value)[:10])
    except ValueError:
        return None


def trade_open_weekday(trade: dict[str, Any]) -> str:
    dt = parse_iso_date(trade.get("visual_open_date") or trade.get("open_date"))
    return dt.strftime("%a") if dt else "Unknown"


def trade_close_weekday(trade: dict[str, Any]) -> str:
    return str(trade.get("inferred_close_weekday") or "Unknown")


def trade_days_held(trade: dict[str, Any]) -> int | None:
    value = trade.get("days_held")
    if value is None:
        return None
    try:
        return int(value)
    except (TypeError, ValueError):
        return None


def trade_pnl_event_date(trade: dict[str, Any]) -> str:
    if not trade.get("is_active") and trade.get("inferred_close_date"):
        return str(trade["inferred_close_date"])[:10]
    for key in ("visual_last_pnl_date", "last_update", "visual_open_date", "open_date"):
        value = trade.get(key)
        if value:
            return str(value)[:10]
    return "Unknown"


def trade_dte_bucket(trade: dict[str, Any]) -> str:
    if trade.get("dte_bucket"):
        return str(trade["dte_bucket"])
    dte = trade.get("visual_dte_open") or trade.get("dte_open")
    if dte is None:
        return "Unknown"
    dte = int(dte)
    if dte <= 10:
        return "7 DTE"
    if dte <= 24:
        return "14/21 DTE"
    if dte <= 32:
        return "28 DTE"
    return "35/42 DTE"


