def _trigger_export_script(*, source: str = "manual") -> bool:
    """Spawns the export pipeline in a background thread.

    Returns True if a refresh was started, False if one was already running.
    Shared by the manual `/api/snapshot/refresh` endpoint and the cron scheduler.
    """
    with _refresh_lock:
        if _refresh_running["state"]:
            print(f"[snapshot refresh:{source}] skipped — already running")
            return False
        _refresh_running["state"] = True

    def run() -> None:
        try:
            print(f"[snapshot refresh:{source}] running export_control_panel.py")
            proc = subprocess.run(
                [
                    sys.executable,
                    str(REPO_ROOT / "scripts" / "export_control_panel.py"),
                    "--gdrive-id",
                    GDRIVE_ID,
                ],
                cwd=str(REPO_ROOT),
                check=False,
                capture_output=True,
                text=True,
                timeout=300,
            )
            if proc.stdout:
                print(proc.stdout, end="")
            if proc.stderr:
                print(proc.stderr, end="", file=sys.stderr)
            print(f"[snapshot refresh:{source}] finished rc={proc.returncode}")
        except subprocess.TimeoutExpired:
            print(f"[snapshot refresh:{source}] exporter timed out after 300s", file=sys.stderr)
        finally:
            _snapshot_cache["data"] = None
            with _refresh_lock:
                _refresh_running["state"] = False

    threading.Thread(target=run, daemon=True).start()
    return True


@app.post("/api/snapshot/refresh")
def refresh_snapshot() -> JSONResponse:
    if not _trigger_export_script(source="manual"):
        return JSONResponse({"status": "already_running"}, status_code=409)
    return JSONResponse({"status": "refresh_started"}, status_code=202)


@app.get("/api/months", response_model=MonthsResponse)
def get_months() -> MonthsResponse:
    snap = load_snapshot()
    sheet_months: dict[str, dict[str, Any]] = {}
    for trade in snap.get("trades", []):
        sheet = (trade.get("sheet") or "").strip()
        if not sheet or not MONTH_SHEET_REGEX.match(sheet):
            continue
        env = trade.get("environment") or SHEET_TO_ENV.get(sheet, "Unknown")
        info = sheet_months.setdefault(sheet, {"sheet": sheet, "env": env, "n_trades": 0, "active": False})
        info["n_trades"] += 1
        if trade.get("is_active"):
            info["active"] = True

    months = [
        MonthInfo(
            sheet=info["sheet"],
            env=info["env"],
            label=_pretty_month_label(info["sheet"]),
            n_trades=info["n_trades"],
            active=info["active"],
        )
        for info in sheet_months.values()
    ]

    months.sort(key=_month_filter_sort_tuple)
    return MonthsResponse(months=months)


@app.get("/api/trades", response_model=TradesResponse)
def get_trades(
    month: str | None = Query(None, description="Comma-separated month codes (e.g. APR26,MAR26)"),
    env: str | None = Query(None, description="Environment filter (CZ_Live, JS_Forward, CZ_Forward)"),
) -> TradesResponse:
    snap = load_snapshot()
    months = parse_months(month)
    trades = [t for t in snap.get("trades", []) if trade_in_filter(t, months, env)]

    # Normalize PnL/delta and backfill display fields for visual-only orphan trades
    # (source=visual_sheet) that don't have a current db_robots row.
    individual_pnls = snap.get("individual_trade_pnls") or {}
    today = datetime.utcnow().date()
    for t in trades:
        t["pnl"] = trade_pnl(t, individual_pnls)
        t["delta"] = trade_delta(t)
        if t.get("dte_open") is None and t.get("visual_dte_open") is not None:
            t["dte_open"] = t["visual_dte_open"]
        if t.get("open_date") is None and t.get("visual_open_date"):
            t["open_date"] = t["visual_open_date"]
        if t.get("exp_date") is None and t.get("visual_exp_date"):
            t["exp_date"] = t["visual_exp_date"]
        if t.get("dte_remaining") is None and t.get("exp_date"):
            exp = parse_iso_date(t["exp_date"])
            if exp:
                t["dte_remaining"] = (exp.date() - today).days

    return TradesResponse(filter=FilterEcho(months=months, env=env), trades=trades)


@app.get("/api/kpis", response_model=KpisResponse)
def get_kpis(
    month: str | None = Query(None),
    env: str | None = Query(None),
) -> KpisResponse:
    snap = load_snapshot()
    months = parse_months(month)
    trades = [t for t in snap.get("trades", []) if trade_in_filter(t, months, env)]
    individual_pnls = snap.get("individual_trade_pnls") or {}

    open_pnl = 0.0
    rlzd = 0.0
    delta_total = 0.0
    max_loss_exposed = 0.0
    net_credit_at_risk = 0.0
    n_active = 0
    n_closed = 0
    closed_pnls: list[float] = []
    daily_theta = 0.0

    for t in trades:
        pnl = trade_pnl(t, individual_pnls)
        is_active = bool(t.get("is_active"))
        if is_active:
            n_active += 1
            open_pnl += pnl
            delta_total += trade_delta(t)
            max_loss_exposed += float(t.get("max_loss") or 0)
            net_credit_at_risk += float(t.get("net_credit") or 0)
            dte = t.get("dte_remaining")
            nc = t.get("net_credit")
            if dte and nc and dte > 0:
                daily_theta += float(nc) / float(dte)
        else:
            n_closed += 1
            rlzd += pnl
            closed_pnls.append(pnl)

    wins = [p for p in closed_pnls if p > 0]
    losses = [p for p in closed_pnls if p < 0]
    win_rate = len(wins) / len(closed_pnls) if closed_pnls else None
    profit_factor = (sum(wins) / abs(sum(losses))) if losses and sum(losses) else None
    expectancy = (sum(closed_pnls) / len(closed_pnls)) if closed_pnls else None

    sheet_summaries = sheet_summary_lookup(snap)
    selected_summaries = [
        sheet_summaries[m]
        for m in months
        if m in sheet_summaries and (env is None or sheet_summaries[m].get("env") == env)
    ]
    # Sheet TOTAL row is authoritative for OpenPnL / RLZD / Delta / MaxProfit when filtering by month.
    # net_credit_at_risk and max_loss_exposed stay trade-level (sum of NC / MaxLoss across active trades).
    max_profit_total = sum(float(t.get("net_credit") or 0) for t in trades if t.get("is_active"))
    if selected_summaries:
        open_pnl = sum(float(s.get("open_pnl") or 0) for s in selected_summaries)
        rlzd = sum(float(s.get("rlzd") or 0) for s in selected_summaries)
        delta_total = sum(float(s.get("delta") or 0) for s in selected_summaries)
        sheet_max_profit = sum(float(s.get("max_profit") or 0) for s in selected_summaries)
        if sheet_max_profit:
            max_profit_total = sheet_max_profit

    return KpisResponse(
        filter=FilterEcho(months=months, env=env),
        pnl={
            "open": round(open_pnl, 2),
            "rlzd": round(rlzd, 2),
            "delta": round(delta_total, 2),
            "max_profit": round(max_profit_total, 2),
        },
        risk={
            "max_loss_exposed": round(max_loss_exposed, 2),
            "net_credit_at_risk": round(net_credit_at_risk, 2),
            "est_daily_theta": round(daily_theta, 2),
        },
        performance={
            "win_rate": round(win_rate, 4) if win_rate is not None else None,
            "profit_factor": round(profit_factor, 2) if profit_factor is not None else None,
            "expectancy": round(expectancy, 2) if expectancy is not None else None,
        },
        trade_intel={
            "best_trade": round(max(closed_pnls), 2) if closed_pnls else None,
            "worst_trade": round(min(closed_pnls), 2) if closed_pnls else None,
            "n_active": n_active,
            "n_closed": n_closed,
        },
    )


@app.get("/api/analytics")
def get_analytics(
    month: str | None = Query(None),
    env: str | None = Query(None),
) -> JSONResponse:
    snap = load_snapshot()
    months = parse_months(month)
    individual_pnls = snap.get("individual_trade_pnls") or {}
    sheet_summaries = sheet_summary_lookup(snap)
    sheet_daily_pnls = snap.get("sheet_daily_pnls") or {}

    def valid_month_trade(t: dict[str, Any]) -> bool:
        sheet = (t.get("sheet") or "").strip()
        if not sheet or not MONTH_SHEET_REGEX.match(sheet):
            return False
        if months and sheet not in months:
            return False
        if env and t.get("environment") != env:
            return False
        return True

    trades = [t for t in snap.get("trades", []) if valid_month_trade(t)]
    months_in_scope = months or sorted({t.get("sheet") for t in trades if t.get("sheet")}, key=lambda s: _month_sort_tuple(str(s)))

    def pnl_for(t: dict[str, Any]) -> float:
        return trade_pnl(t, individual_pnls)

    def aggregate(key_fn, source_trades: list[dict[str, Any]] | None = None) -> list[dict[str, Any]]:
        groups: dict[str, dict[str, Any]] = {}
        rows = source_trades if source_trades is not None else trades
        for t in rows:
            key = key_fn(t) or "Unknown"
            pnl = pnl_for(t)
            g = groups.setdefault(key, {"key": key, "pnl": 0.0, "n_trades": 0, "wins": 0, "losses": 0, "active": 0})
            g["pnl"] += pnl
            g["n_trades"] += 1
            g["wins"] += int(pnl > 0)
            g["losses"] += int(pnl < 0)
            g["active"] += int(bool(t.get("is_active")))
        rows = []
        for g in groups.values():
            n = g["n_trades"]
            rows.append({
                **g,
                "pnl": round(g["pnl"], 2),
                "win_rate": round(g["wins"] / n, 4) if n else None,
                "avg_pnl": round(g["pnl"] / n, 2) if n else None,
            })
        return sorted(rows, key=lambda r: abs(float(r["pnl"])), reverse=True)

    pnls = [pnl_for(t) for t in trades]
    wins = [p for p in pnls if p > 0]
    losses = [p for p in pnls if p < 0]
    total_pnl = sum(pnls)
    profit_factor = (sum(wins) / abs(sum(losses))) if losses and sum(losses) else None

    by_month = []
    for sheet in months_in_scope:
        if not sheet:
            continue
        month_trades = [t for t in trades if t.get("sheet") == sheet]
        summary = sheet_summaries.get(str(sheet), {})
        pnl = float(summary.get("open_pnl") or 0) + float(summary.get("rlzd") or 0) if summary else sum(pnl_for(t) for t in month_trades)
        by_month.append({
            "key": sheet,
            "label": _pretty_month_label(str(sheet)),
            "pnl": round(pnl, 2),
            "open_pnl": round(float(summary.get("open_pnl") or 0), 2) if summary else 0.0,
            "rlzd": round(float(summary.get("rlzd") or 0), 2) if summary else 0.0,
            "n_trades": len(month_trades),
            "active": any(t.get("is_active") for t in month_trades),
        })

    by_strategy = aggregate(lambda t: strategy_family(str(t.get("name", ""))))
    by_underlying = aggregate(lambda t: t.get("underlying") or "Unknown")
    by_dte_bucket = aggregate(trade_dte_bucket)
    by_weekday = aggregate(trade_open_weekday)
    by_day = []
    daily_groups: dict[str, dict[str, Any]] = {}
    has_total_daily_path = False
    for sheet in months_in_scope:
        for row in sheet_daily_pnls.get(str(sheet), []) or []:
            day = str(row.get("date") or "")
            if not day:
                continue
            has_total_daily_path = has_total_daily_path or row.get("pnl_type") == "total_open_plus_rlzd" or "open_pnl" in row or "rlzd" in row
            g = daily_groups.setdefault(
                day,
                {"key": day, "pnl": 0.0, "open_pnl": 0.0, "rlzd": 0.0, "daily_pnl": 0.0, "n_trades": 0, "trades": []},
            )
            g["pnl"] += float(row.get("pnl") or 0)
            g["open_pnl"] += float(row.get("open_pnl") or 0)
            g["rlzd"] += float(row.get("rlzd") or 0)
            g["daily_pnl"] += float(row.get("daily_pnl") or row.get("pnl") or 0)
            g["n_trades"] += int(row.get("n_trades") or 0)
            g["trades"].extend(row.get("trades") or [])
    if daily_groups:
        for row in sorted(daily_groups.values(), key=lambda r: str(r["key"])):
            pnl = round(float(row["pnl"]), 2)
            if has_total_daily_path:
                by_day.append({
                    **row,
                    "pnl": pnl,
                    "open_pnl": round(float(row.get("open_pnl") or 0), 2),
                    "rlzd": round(float(row.get("rlzd") or 0), 2),
                    "daily_pnl": round(float(row.get("daily_pnl") or 0), 2),
                })
            else:
                by_day.append({**row, "pnl": pnl})
    else:
        by_day_raw = aggregate(trade_pnl_event_date)
        cumulative_pnl = 0.0
        for row in sorted(by_day_raw, key=lambda r: str(r["key"])):
            cumulative_pnl += float(row["pnl"])
            by_day.append({**row, "cumulative_pnl": round(cumulative_pnl, 2)})
    closed_trades = [t for t in trades if not t.get("is_active")]
    closed_with_close_weekday = [t for t in closed_trades if trade_close_weekday(t) != "Unknown"]
    by_close_weekday = aggregate(trade_close_weekday, closed_with_close_weekday)
    days_held = [d for t in closed_trades if (d := trade_days_held(t)) is not None]

    top_winners = sorted([t for t in trades if pnl_for(t) > 0], key=pnl_for, reverse=True)[:5]
    top_losers = sorted([t for t in trades if pnl_for(t) < 0], key=pnl_for)[:5]

    insights = []
    if by_strategy:
        best_strategy = max(by_strategy, key=lambda r: float(r["pnl"]))
        worst_strategy = min(by_strategy, key=lambda r: float(r["pnl"]))
        insights.append({"label": "Best strategy family", "value": best_strategy["key"], "detail": best_strategy["pnl"]})
        insights.append({"label": "Worst strategy family", "value": worst_strategy["key"], "detail": worst_strategy["pnl"]})
    if by_dte_bucket:
        best_dte_bucket = max(by_dte_bucket, key=lambda r: float(r["pnl"]))
        insights.append({"label": "Best DTE bucket", "value": best_dte_bucket["key"], "detail": best_dte_bucket["pnl"]})
    if losses:
        insights.append({"label": "Loss concentration", "value": f"{abs(min(losses)) / abs(sum(losses)):.0%}", "detail": "largest loss / total gross losses"})

    return JSONResponse({
        "filter": {"months": months, "env": env},
        "summary": {
            "total_pnl": round(total_pnl, 2),
            "n_trades": len(trades),
            "n_active": sum(1 for t in trades if t.get("is_active")),
            "win_rate": round(len(wins) / len(pnls), 4) if pnls else None,
            "avg_win": round(sum(wins) / len(wins), 2) if wins else None,
            "avg_loss": round(sum(losses) / len(losses), 2) if losses else None,
            "profit_factor": round(profit_factor, 2) if profit_factor is not None else None,
            "avg_days_held": round(sum(days_held) / len(days_held), 2) if days_held else None,
        },
        "by_month": by_month,
        "by_strategy": by_strategy,
        "by_underlying": by_underlying,
        "by_dte_bucket": by_dte_bucket,
        "by_weekday": by_weekday,
        "by_day": by_day,
        "by_close_weekday": by_close_weekday,
        "top_winners": [_analytics_trade(t, pnl_for(t)) for t in top_winners],
        "top_losers": [_analytics_trade(t, pnl_for(t)) for t in top_losers],
        "insights": insights,
    })


