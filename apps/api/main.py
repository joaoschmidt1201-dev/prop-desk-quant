async def _stream_anthropic(system: str, messages: list[dict[str, str]]) -> Iterable[bytes]:
    api_key = os.getenv("ANTHROPIC_API_KEY")
    if not api_key:
        yield _sse({"error": "ANTHROPIC_API_KEY not set", "done": True})
        return
    try:
        from anthropic import AsyncAnthropic
    except ImportError:
        yield _sse({"error": "anthropic SDK not installed", "done": True})
        return

    client = AsyncAnthropic(api_key=api_key)
    try:
        async with client.messages.stream(
            model="claude-sonnet-4-6",
            max_tokens=4096,
            system=system,
            messages=messages,
            tools=[{"type": "web_search_20250305", "name": "web_search", "max_uses": 5}],
        ) as stream:
            async for text in stream.text_stream:
                yield _sse({"delta": text, "done": False})
        yield _sse({"done": True})
    except Exception as exc:
        yield _sse({"error": f"Anthropic API error: {exc}", "done": True})


async def _stream_openai(system: str, messages: list[dict[str, str]]) -> Iterable[bytes]:
    api_key = os.getenv("OPENAI_API_KEY")
    if not api_key:
        yield _sse({"error": "OPENAI_API_KEY not set", "done": True})
        return
    try:
        from openai import AsyncOpenAI
    except ImportError:
        yield _sse({"error": "openai SDK not installed", "done": True})
        return

    client = AsyncOpenAI(api_key=api_key)
    response_input = [
        {"role": m["role"], "content": m["content"]}
        for m in messages
        if m["role"] in ("user", "assistant")
    ]
    try:
        stream = await client.responses.create(
            model="gpt-5.5",
            instructions=system,
            input=response_input,
            tools=[{"type": "web_search"}],
            tool_choice="auto",
            reasoning={"effort": "medium"},
            text={"verbosity": "medium"},
            max_output_tokens=4096,
            stream=True,
        )
        async for event in stream:
            if getattr(event, "type", None) == "response.error":
                error = getattr(event, "error", None)
                yield _sse({"error": f"OpenAI API error: {error}", "done": True})
                return
            if getattr(event, "type", None) != "response.output_text.delta":
                continue
            delta = getattr(event, "delta", None)
            if delta:
                yield _sse({"delta": delta, "done": False})
        yield _sse({"done": True})
    except Exception as exc:
        yield _sse({"error": f"OpenAI API error: {exc}", "done": True})


def _sse(payload: dict[str, Any]) -> bytes:
    return f"data: {json.dumps(payload, ensure_ascii=False)}\n\n".encode("utf-8")
