def _env_flag(name: str, default: str = "0") -> bool:
    return os.getenv(name, default).strip().lower() in ("1", "true", "yes", "on")


@asynccontextmanager
async def lifespan(_app: FastAPI) -> AsyncIterator[None]:
    """Boots the snapshot scheduler when SCHEDULER_ENABLED is truthy.

    Cron: every SCHEDULER_INTERVAL_MIN minutes, weekdays, between SCHEDULER_HOURS
    (default 9-16) in SCHEDULER_TZ (default America/New_York). Each tick triggers
    the same export pipeline as the manual refresh button — same lock, same cache
    invalidation, so doubles can't pile up.
    """
    scheduler = None
    if _env_flag("SCHEDULER_ENABLED"):
        try:
            from apscheduler.schedulers.background import BackgroundScheduler
            from apscheduler.triggers.cron import CronTrigger
        except ImportError:
            print("[scheduler] apscheduler not installed — skip", file=sys.stderr)
        else:
            tz = os.getenv("SCHEDULER_TZ", "America/New_York")
            interval = max(1, int(os.getenv("SCHEDULER_INTERVAL_MIN", "10")))
            hours = os.getenv("SCHEDULER_HOURS", "9-16")
            days = os.getenv("SCHEDULER_DAYS", "mon-fri")
            scheduler = BackgroundScheduler(timezone=tz)
            scheduler.add_job(
                lambda: _trigger_export_script(source="scheduler"),
                CronTrigger(
                    minute=f"*/{interval}",
                    hour=hours,
                    day_of_week=days,
                    timezone=tz,
                ),
                id="snapshot_refresh",
                max_instances=1,
                coalesce=True,
                misfire_grace_time=300,
            )
            scheduler.start()
            print(f"[scheduler] started — */{interval}min, hours={hours}, days={days}, tz={tz}")
    else:
        print("[scheduler] disabled (set SCHEDULER_ENABLED=1 to enable)")

    try:
        yield
    finally:
        if scheduler is not None:
            scheduler.shutdown(wait=False)
            print("[scheduler] stopped")


